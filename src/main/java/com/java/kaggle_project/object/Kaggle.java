package com.java.kaggle_project.object;

public class Kaggle {
    String datasetName;

    String category;
    int dataset_id;

    String url;
    String dataset_location;

    public String getDatasetName() {
        return datasetName;
    }

    public void setDatasetName(String datasetName) {
        this.datasetName = datasetName;
    }


    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public int getDataset_id() {
        return dataset_id;
    }

    public void setDataset_id(int dataset_id) {
        this.dataset_id = dataset_id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getDataset_location() {
        return dataset_location;
    }

    public void setDataset_location(String dataset_location) {
        this.dataset_location = dataset_location;
    }

}
